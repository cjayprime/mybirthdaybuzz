            <h1>About MBB</h1>

            <p>My Birthday Buzz &reg;</p>
            The space between birth and death is unarguably a major connecting attribute of the human existence. Over the years, it has been a very common overrated practice to pay tribute to the dead above the importance of paying such tributes to people while they are alive. Every human being desires and deserves to be celebrated. Hence the need to place so much importance on the contribution to this especially in the space between life and death which is one of the common attributes of human existence.
            <br />
            My Birthday Buzz (MBB) launched out in October 2015; first on social media on account page names @mybirthdaybuzz across Instagram, facebook and twitter. It moved to radio in January 2016 before transcending into this larger website platform.
            <br />
            MBB is an idea created to place and encourage high priority on the celebration and appreciation of human life every day and since it is always someone's birthday each day, such celebration becomes endless.
			
